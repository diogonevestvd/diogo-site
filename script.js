function applyLanguage(lang) {
  const dict = translations[lang] || translations.pt;
  document.documentElement.lang = lang;
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    if (dict[key]) el.textContent = dict[key];
  });
  document.querySelectorAll("[data-lang-option]").forEach((el) => {
    el.classList.toggle("is-active", el.dataset.langOption === lang);
  });
  localStorage.setItem("siteLanguage", lang);
}

document.addEventListener("DOMContentLoaded", () => {
  const saved = localStorage.getItem("siteLanguage") || "pt";
  applyLanguage(saved);

  document.querySelectorAll("[data-lang-option]").forEach((button) => {
    button.addEventListener("click", () => {
      applyLanguage(button.dataset.langOption);
    });
  });
});


function syncLanguagePills(lang) {
  document.querySelectorAll("[data-lang-option]").forEach((el) => {
    el.classList.toggle("is-active", el.dataset.langOption === lang);
  });
}

document.addEventListener("DOMContentLoaded", () => {
  const menuToggle = document.querySelector(".menu-toggle");
  const menuClose = document.querySelector(".menu-close");
  const mobileOverlay = document.querySelector(".mobile-menu-overlay");

  if (menuToggle && mobileOverlay) {
    menuToggle.addEventListener("click", () => {
      mobileOverlay.hidden = false;
      mobileOverlay.classList.add("is-open");
      menuToggle.setAttribute("aria-expanded", "true");
    });
  }

  if (menuClose && mobileOverlay) {
    menuClose.addEventListener("click", () => {
      mobileOverlay.classList.remove("is-open");
      mobileOverlay.hidden = true;
      if (menuToggle) menuToggle.setAttribute("aria-expanded", "false");
    });
  }

  if (mobileOverlay) {
    mobileOverlay.addEventListener("click", (e) => {
      if (e.target === mobileOverlay) {
        mobileOverlay.classList.remove("is-open");
        mobileOverlay.hidden = true;
        if (menuToggle) menuToggle.setAttribute("aria-expanded", "false");
      }
    });

    mobileOverlay.querySelectorAll("a").forEach((link) => {
      link.addEventListener("click", () => {
        mobileOverlay.classList.remove("is-open");
        mobileOverlay.hidden = true;
        if (menuToggle) menuToggle.setAttribute("aria-expanded", "false");
      });
    });
  }

  const saved = localStorage.getItem("siteLanguage") || "pt";
  syncLanguagePills(saved);

  document.querySelectorAll("[data-lang-option]").forEach((button) => {
    button.addEventListener("click", () => {
      applyLanguage(button.dataset.langOption);
      syncLanguagePills(button.dataset.langOption);
    });
  });
});
